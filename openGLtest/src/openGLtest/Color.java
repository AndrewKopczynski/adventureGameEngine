package openGLtest;

public class Color
{
	private float m_r = 0.0f;
	private float m_g = 0.0f;
	private float m_b = 0.0f;
	
	public float getR()
	{
		return m_r;
	}
	public void setR(float red)
	{
		m_r = red;
	}
	
	public float getG()
	{
		return m_g;
	}
	public void setG(float grn)
	{
		m_g = grn;
	}
	
	public float getB()
	{
		return m_b;
	}
	public void setB(float blu)
	{
		m_b = blu;
	}
}
